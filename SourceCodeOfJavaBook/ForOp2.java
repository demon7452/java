public class ForOp2
{
	public static void main(String args[])
	{
		int i,j,k,m;
		System.out.println("i\t"+"j\t"+"k\t"+"m\t");
		for (i=0;i<2;i++)
		   for(j=0;j<2;j++)
		   	  for (k=0;k<2;k++)
		   	     for (m=0;m<2;m++)
		   	  {
		   	  	System.out.println(i+"\t"+j+"\t"+k+"\t"+m+"\t");
		   	  	
		   	  }
	}
}